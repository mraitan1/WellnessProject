export function getDailyQuote() {
    const now = new Date();
    // Create a number from the date: e.g. 20260501
    const seed = now.getFullYear() * 10000 + (now.getMonth() + 1) * 100 + now.getDate();
    return quotes[seed % quotes.length];
}